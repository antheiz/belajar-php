<?php

$angka1 = 10;
$angka2 = 5;

// perkalian
var_dump($angka1 * $angka2);

// penjumlahan
var_dump($angka1 + $angka2);

// pengurangan
var_dump($angka1 - $angka2);

// pembagian
var_dump($angka1 / $angka2);

// sisa bagi
var_dump($angka1 % $angka2);

// pangkatan
var_dump($angka1 ** $angka2);

// positif/negatif
var_dump(+$angka1);
var_dump(-$angka1);